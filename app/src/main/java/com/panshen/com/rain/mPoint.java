package com.panshen.com.rain;


public class mPoint {

    private float x;

    private float y;

    public mPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }
    public mPoint() {
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

}
